package MyPackage;

//8. В массив A [N] занесены натуральные числа. Найти сумму тех элементов, которые кратны данному К.
public class Worker {
	int k = 3;
	int[] mass = { 2, 4, 6, 10, 3, 6, 9, 12 };
	int sum = 0;

	public void counting() {

		for (int i = 0; i < mass.length; i++) { // Пробегаемся по всем элементам
												// массива
			if (mass[i] % k == 0) { // Если число кратно k
				sum = sum + mass[i];

			}

		}

		System.out.println("Сумма чисел  " + sum);

	}
}
